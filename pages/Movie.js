class Movie {
    constructor(title, year, genre, plot, director, actors, imdbRating, poster) {
      this.title = title;
      this.year = year;
      this.genre = genre;
      this.plot = plot;
      this.director = director;
      this.actors = actors;
      this.imdbRating = imdbRating;
      this.poster = poster;
    }
  }
  export default Movie;